/**
 * ASCII-баннер «лица» Искина в консоли (отсылка к SHODAN / System Shock).
 * Фаза меняется каждые ~3 с — лёгкое мерцание «энергии» и зрачков.
 */

export function getIskinDialogBannerLines(phase: number): string[] {
  const p = ((phase % 3) + 3) % 3;
  const l = p === 0 ? "/" : p === 1 ? "|" : "\\";
  const r = p === 0 ? "\\" : p === 1 ? "|" : "/";
  const eyeL = p === 0 ? "||" : p === 1 ? "││" : "||";
  const eyeR = p === 0 ? "||" : p === 1 ? "││" : "||";

  return [
    "",
    "  ╔══════════════════════════════════════════════════════════╗",
    `  ║  ${l}${l}${l} ░░░ CIRCUIT // SUBJECT: ISKIN ░░░ ${r}${r}${r}               ║`,
    "  ║      \\\\   ~~ neural uplink / staging   //                ║",
    "  ║   ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░                     ║",
    "  ║  ░    ╭────────────────────────╮    ░                    ║",
    "  ║  ░   ╱  -+--+--+  ~~  -+--+--+  ╲   ░   «лицо» узла      ║",
    `  ║  ░  │ (${eyeL}  ██  ${eyeL})  (${eyeR}  ██  ${eyeR}) |  ░   следят за сессией║`,
    "  ║  ░   ╲   ╲█|█╱         ╲█|█╱    ╱   ░                    ║",
    "  ║  ░    ╰──────────╯──╯──────────╯    ░                    ║",
    "  ║   ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░                     ║",
    `  ║     ${l}  кабели / зелёный спектр / не отключайте  ${r}        ║`,
    "  ╚══════════════════════════════════════════════════════════╝",
    "",
  ];
}
