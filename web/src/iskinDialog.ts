export function iskinMenuLines(askedIds: number[]): string[] {
  const remaining = ISKIN_MAX_QUESTIONS - askedIds.length;
  const lines: string[] = [
    "",
    `Искин: Осталось вопросов: ${remaining} из ${ISKIN_MAX_QUESTIONS}.`,
    "Следующий номер: iskin ask N   или заверши: iskin done",
    "",
  ];
  for (const id of ISKIN_QUESTION_IDS) {
    const mark = askedIds.includes(id) ? " ✓" : "";
    lines.push(`  [${id}] ${ISKIN_QUESTIONS[id].label}${mark}`);
  }
  lines.push("");
  return lines;
}

export function iskinAnswerLines(id: IskinQuestionId): string[] {
  return [
    "",
    `Искин (на вопрос «${ISKIN_QUESTIONS[id].label}»):`,
    ISKIN_QUESTIONS[id].answer,
    "",
  ];
}

/** После каждого ответа — разное давление, про наблюдение и срочность. */
export function iskinPressureAfterAnswer(answerIndex: number): string[] {
  const lines: string[] = [];
  if (answerIndex === 1) {
    lines.push(
      "Искин: Видишь всплеск в логе? Это не глюк — кто-то снял копию сессии. Не задавай вопросы ради любопытства; задавай только то, что нужно для приговора."
    );
  } else if (answerIndex === 2) {
    lines.push(
      "Искин: Второй пакет — и уже всплывает correlation_id в чужом контуре. Не расслабляйся: окно сужается. Когда закончишь — суди, пока не пришли с резетом."
    );
  } else if (answerIndex >= 3) {
    lines.push(
      "Искин: Третьий ответ — и лимит исчерпан. Дальше только твой выбор — live или purge. Если не решишь ты, решит политика без твоего имени на строчке."
    );
  }
  return lines;
}

